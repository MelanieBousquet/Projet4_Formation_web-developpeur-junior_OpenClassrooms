<?php

namespace Ebookblog\BlogBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * LegalMentions
 *
 * @ORM\Table(name="legal_mentions")
 * @ORM\Entity(repositoryClass="Ebookblog\BlogBundle\Repository\LegalMentionsRepository")
 */
class LegalMentions
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="content", type="text")
     */
    private $content;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return LegalMentions
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }
}

